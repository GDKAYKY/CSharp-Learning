using System;

namespace PrintLiteralValues
{
    class PrintLiteralValuesClass
    {
        public static void PrintLiteralValuesMethod(string[] args)
        {
            // Print different literal data types

                // Use integer literals
                    Console.WriteLine('n'); 
                    Console.WriteLine(456);
                
                // Use floating-point literals
                    Console.WriteLine(0.5F);
                    Console.WriteLine(3.435);
                    Console.WriteLine(12.39816m);

                // Use Boolean literals
                    Console.WriteLine(true);
                    Console.WriteLine(false);

                // Data types define capabilities
                    Console.WriteLine("321");
                    Console.WriteLine(321);
                    Console.WriteLine("false");
                    Console.WriteLine(false);
        }
    }
}